class Solution {
  public:
    // Function to find maximum product subarray
    
    int maxProduct(vector<int> &a) {
        // Your Code Here
        int n=a.size();
        int maxP = a[0], minP=a[0], res=a[0];
        
        for(int i=1;i<n;++i){
            if(a[i]<0) {swap(maxP,minP);}
            maxP=max(a[i],maxP*a[i]);
            minP=min(a[i],minP*a[i]);
            res=max(res,maxP);
        }
        return res;
    }
};


